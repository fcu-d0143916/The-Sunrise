package tw.edu.fcu.lukeway.thesunrise;

public class sun {

    private String no;

    private String spec;

    private String data;

    private String sunup;

    private String sundown;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getSpec() {
        return spec;
    }

    public void setSpec(String spec) {
        this.spec = spec;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getSunup() {
        return sunup;
    }

    public void setSunup(String sunup) {
        this.sunup = sunup;
    }

    public String getSundown() {
        return sundown;
    }

    public void setSundown(String sundown) {
        this.sundown = sundown;
    }
}
